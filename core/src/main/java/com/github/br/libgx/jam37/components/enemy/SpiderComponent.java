package com.github.br.libgx.jam37.components.enemy;

import com.artemis.Component;
import com.badlogic.gdx.math.Vector2;
import com.badlogic.gdx.physics.box2d.Body;

public class SpiderComponent extends Component {

    public float speed = 1.5f;

    // Физические кости
    public Body prosoma;
    public Body opisthosoma;
    public Body cheliceraLeft;
    public Body cheliceraRight;

    // Динамические точки лап
    public final Vector2[] currentFootPos = new Vector2[8];
    public final Vector2[] targetFootPos = new Vector2[8];
    public final float[] legTimers = new float[8];

    // Живые углы суставов, которые будут плавно лерпиться в процессе ходьбы (GC Free)
    public final float[] currentFemurAngles = new float[8];
    public final float[] currentTibiaAngles = new float[8];

    // =========================================================================
    // ТВОЯ НОВАЯ АНАТОМИЧЕСКАЯ ПАНЕЛЬ НАСТРОЕК (Прямой контроль скелета!)
    // Индексы массивов: 0 - Задняя (IV), 1 - Средняя-задняя (III), 2 - Средняя-передняя (II), 3 - Передняя (I)
    // =========================================================================

    // --- ШАГ 1: НАСТРОЙКА ДЛИНЫ КАЖДОЙ КОСТИ (в метрах Box2D) ---
    public final float[] femurLengthConfig = new float[] {
        0.45f, // Длина бедра задней ноги
        0.40f, // Длина бедра средней-задней
        0.45f, // Длина бедра средней-передней
        0.50f  // Длина бедра передней ноги
    };

    public final float[] tibiaLengthConfig = new float[] {
        0.80f, // Длина голени задней ноги
        0.75f, // Длина голени средней-задней
        0.55f, // Длина голени средней-передней
        0.60f  // Длина голени передней ноги
    };

    public final float[] tarsusLengthConfig = new float[] {
        0.40f, // Длина лапки (кончика) задней ноги
        0.45f, // Длина лапки средней-задней
        0.40f, // Длина лапки средней-передней
        0.40f  // Длина лапки передней ноги
    };


    // --- ШАГ 2: НАСТРОЙКА УГЛОВ ПОВОРОТА (в градусах) ---
    // Угол 0° — это направление взгляда паука (вверх/вперед).
    // Настройки пишутся для ПРАВОЙ стороны паука. ЛЕВАЯ сторона отзеркалится автоматически!

    // 1. Угол выхода бедра из головогруди.
    public final float[] femurAngleConfig = new float[] {
        125f,  // Задняя нога торчит назад-вправо
        110f,  // Средняя-задяя торчит вправо-назад
        75f,   // Средняя-передняя торчит вправо-вперед
        55f    // Передняя нога торчит вперед-вправо
    };

    // 2. Угол излома в первом колене (Голень относительно Бедра).
    // Положительный угол гнет ногу наружу/вбок, отрицательный — внутрь.
    public final float[] tibiaAngleConfig = new float[] {
        40f,  // Излом колена задней ноги
        45f,  // Излом колена средней-задней
        -40f,   // Излом колена средней-передней
        -45f    // Излом колена передней ноги
    };

    // 3. Угол излома кончика лапки (Лапка относительно Голени).
    // Именно этот параметр решит твою задачу: завернет задние лапки внутрь, а передние растопырит наружу!
    public final float[] tarsusAngleConfig = new float[] {
        40f,  // Задняя лапка сильно загибается ВНУТРЬ к брюшку
        35f,  // Средняя-задняя загибается внутрь
        -25f,   // Средняя-передняя растопыривается НАРУЖУ
        -25f    // Передняя лапка сильно растопыривается НАРУЖУ от лица
    };

    // --- ШАГ 3: НАСТРОЙКА АМПЛИТУДЫ (РАЗМАХА) ШАГА (в градусах) ---
    // Индексы массивов: 0 - Задняя (IV), 1 - Средняя-задняя (III), 2 - Средняя-передняя (II), 3 - Передняя (I)

    // 1. Максимальный размах покачивания БЕДРА (Femur) вперед/назад.
    // Чем меньше цифра, тем меньше нога «выносится» по ходу движения.
    public final float[] femurAmplitudeConfig = new float[] {
        8f,  // Размах бедра задней ноги
        8f,  // Размах бедра средней-задней
        8f,  // Размах бедра средней-передней
        6f   // Размах бедра передней ноги
    };

    // 2. Максимальный размах излома ГОЛЕНИ (Tibia) вверх/вниз (активность колена).
    // Уменьши эти цифры (например, до 15-25), чтобы колени сгибались скромнее и аккуратнее.
    public final float[] tibiaAmplitudeConfig = new float[] {
        16f, // Размах колена задней ноги (сделай поменьше, чтобы не было дикого излома)
        16f, // Размах колена средней-задней
        16f, // Размах колена средней-передней
        16f  // Размах колена передней ноги
    };

    public SpiderComponent() {
        for (int i = 0; i < 8; i++) {
            currentFootPos[i] = new Vector2();
            targetFootPos[i] = new Vector2();
        }
    }

}
